    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            MetodosPaciente metodos = new PacienteImplementacion();
            String nombreCompleto = txtNombre.getText() + " "
                    + txtApellidoPaterno.getText() + " "
                    + txtApellidoMaterno.getText();
            Paciente paciente = new Paciente(Integer.parseInt(txtId.getText()),
                    Integer.parseInt(txtNSS.getText()),
                    nombreCompleto,
                    Integer.parseInt(txtEdad.getText()),
                    Float.valueOf(txtAltura.getText()));

            JOptionPane.showMessageDialog(rootPane,
                    "Se ha registrado " + metodos.registrarPaciente(paciente) + " satisfactoriamente",
                    "Registro realizado de manera exitosa",
                    JOptionPane.INFORMATION_MESSAGE);
            listarRegistros();
            limpiarInsertar();
        } catch (NumberFormatException err) {
            JOptionPane.showMessageDialog(rootPane,
                    "Error al convertir los datos\n"
                    + "Se han ingresado datos invalidos para los datos numericos (edad, altura, nss)\n"
                    + "Favor de verificar detenidamente.\n"
                    + "Error: " + err.getMessage());
        } catch (Exception ex) {
            Logger.getLogger(InterfazPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

