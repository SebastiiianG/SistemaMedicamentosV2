    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        MetodosPaciente metodos = new PacienteImplementacion();
        String nombreCompleto = txtNombre2.getText() + " "
                + txtApellidoPaterno2.getText() + " "
                + txtApellidoMaterno2.getText();
        Paciente paciente = new Paciente(Integer.parseInt(txtId2.getText()),
                Integer.parseInt(txtNSS2.getText()),
                nombreCompleto,
                Integer.parseInt(txtEdad2.getText()),
                Float.valueOf(txtAltura2.getText()));
        try {

            JOptionPane.showMessageDialog(rootPane,
                    "Se ha actualizado " + metodos.actualizarPaciente(paciente) + " satisfactoriamente",
                    "Registro realizado de manera exitosa",
                    JOptionPane.INFORMATION_MESSAGE);
            listarRegistros();
            limpiarEditar();
        } catch (NumberFormatException err) {
            JOptionPane.showMessageDialog(rootPane,
                    "Error al convertir los datos\n"
                    + "Se han ingresado datos invalidos para los datos numericos (edad, altura, nss)\n"
                    + "Favor de verificar detenidamente.\n"
                    + "Error: " + err.getMessage());
        } catch (Exception ex) {
            Logger.getLogger(InterfazPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here
        if (JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el paciente?\n"
                + "Esta accion no se puede deshacer...",
                "Confirmar eliminacion",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            MetodosPaciente metodos = new PacienteImplementacion();
            try {
                if (metodos.eliminarPaciente(Integer.parseInt(txtId1.getText())) != -1) {
                    JOptionPane.showMessageDialog(rootPane,
                            "Se ha eliminado de manera satisfactoria",
                            "Eliminación satisfactoria",
                            JOptionPane.INFORMATION_MESSAGE
                    );
                    limpiarEliminar();
                    listarRegistros();
                }
            } catch (Exception err) {
                JOptionPane.showMessageDialog(rootPane,
                        "Ha ocurrido un error al eliminar\n"
                        + "Error: " + err.getMessage());
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

