    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        MetodosMedico metodosMedico = new MedicoImplementacion();
        if (validarDatos()) {
            String direccionCompleta = txtCalleDirMed.getText() + ", "
                    + txtNumExtDirMed.getText() + ", "
                    + txtNumeroIntDirMed.getText() + ", "
                    + txtColDirMed.getText() + ", "
                    + txtCPDirMed.getText() + ", "
                    + txtCiudadDirMed.getText();
            String nombreCompleto = txtNombreMedico.getText() + " "
                    + txtApellidoPaternoMedico.getText() + " "
                    + txtApellidoMaternoMedico.getText();
            Medico medico = new Medico(nombreCompleto,
                    txtCedulaMedico.getText(),
                    txtTelefonoMedico.getText(),
                    txtEspecialidadMedico.getText(),
                    direccionCompleta);
            try {
                metodosMedico.registrarMedico(medico);
                listarRegistros(cmbFiltroMedico.getSelectedIndex(),
                        txtCriterioMedico.getText(),
                        cmbOrdenMedico.getSelectedIndex());
                JOptionPane.showMessageDialog(rootPane,
                        "Datos del medico actualizados correctamente",
                        "Registro satisfactorio",
                        JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                Logger.getLogger(InterfazMedico.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Datos numéricos inválidos",
                    "Error de tipo de dato",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnInsertarActionPerformed

