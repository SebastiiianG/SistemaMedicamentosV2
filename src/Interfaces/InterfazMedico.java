    public ArrayList<String> separarEnPalabras(String texto) {
        // Usamos el m√©todo split para dividir el string en palabras usando espacios en blanco como delimitador
        String[] palabrasArray = texto.split("\\s+");

        // El array se convierte en ArrayList para trabajar con √©l m√°s facilmente
        ArrayList<String> palabrasList = new ArrayList<>(Arrays.asList(palabrasArray));

        return palabrasList;
    }

    public ArrayList<String> separarDireccionPalabras(String direccion) {
        // Separamos el string utilizando la coma y espacio como delimitadores
        String[] partes = direccion.split(",\\s*");

        // Convertimos el array en un ArrayList y lo devolvemos
        return new ArrayList<>(Arrays.asList(partes));
    }

